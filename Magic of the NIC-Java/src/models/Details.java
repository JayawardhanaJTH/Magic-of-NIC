package models;

public class Details
{
    private int Year;
    private int Month;
    private int Day;
    private String Gender;


    public int getYear()
    {
        return Year;
    }

    public void setYear(int year)
    {
        Year = year;
    }

    public int getMonth()
    {
        return Month;
    }

    public void setMonth(int month)
    {
        Month = month;
    }

    public int getDay()
    {
        return Day;
    }

    public void setDay(int day)
    {
        Day = day;
    }

    public String getGender()
    {
        return Gender;
    }

    public void setGender(String gender)
    {
        Gender = gender;
    }
}
